class India:
    def capital(self):
        print("New Delhi is the capital of India")
    def language(self):
        print("Hindi is the most commonly spoken language in India")
class USA:
    def capital(self):
        print("Washington D.C is the modern capital of USA")
    def language(self):
        print("English and French are the most commonly spoken language in USA")
R = India()
K = USA()
R.capital()
R.language()
K.capital()
K.language()

